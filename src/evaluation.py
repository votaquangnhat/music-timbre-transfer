def parse_args():
    parser = argparse.ArgumentParser(
        description="Evaluate Riffusion LoRA timbre transfer on test set."
    )

    # Evaluation mode
    parser.add_argument(
        "--mode",
        type=str,
        default="paired",
        choices=["paired", "external"],
        help=(
            "paired: compare generated output to same maestro_id/slice target spectrogram. "
            "external: compare generated output to reference generated by src.ismir2024.genrerate."
        ),
    )

    # Dataset
    parser.add_argument("--spectrograms_dir", type=str, default="data/spectrogram")
    parser.add_argument("--subset", type=str, default="test")
    parser.add_argument("--source_timbre", type=str, required=True)
    parser.add_argument("--target_timbre", type=str, required=True)
    parser.add_argument("--max_samples", type=int, default=None)
    parser.add_argument("--start_index", type=int, default=0)

    # External model mode
    parser.add_argument(
        "--external_target_subset",
        type=str,
        default="test",
        help="Subset used to choose target/style audio slice for external mode.",
    )
    parser.add_argument(
        "--external_target_index",
        type=int,
        default=0,
        help="Index of target-timbre slice used as path2 for external mode.",
    )
    parser.add_argument(
        "--external_nb_steps",
        type=int,
        default=40,
        help="Number of diffusion steps for external ISMIR model.",
    )
    parser.add_argument(
        "--external_guidance",
        type=float,
        default=2.0,
        help="Classifier-free guidance for external ISMIR model.",
    )

    # Model
    parser.add_argument(
        "--pretrained_model_name_or_path",
        type=str,
        default="riffusion/riffusion-model-v1",
    )
    parser.add_argument("--lora_path", type=str, required=True)

    # Inference
    parser.add_argument("--prompt", type=str, default=None)
    parser.add_argument("--strength", type=float, default=0.3)
    parser.add_argument("--guidance_scale", type=float, default=3.0)
    parser.add_argument("--num_inference_steps", type=int, default=50)
    parser.add_argument("--seed", type=int, default=42)

    # Output
    parser.add_argument("--output_dir", type=str, required=True)
    parser.add_argument("--save_examples", action="store_true")
    parser.add_argument("--num_examples_to_save", type=int, default=5)
    parser.add_argument("--save_audio_examples", action="store_true")
    parser.add_argument("--num_griffin_lim_iters", type=int, default=32)

    return parser.parse_args()


