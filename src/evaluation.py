def main():
    args = parse_args()

    spectrograms_dir = Path(args.spectrograms_dir)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    examples_dir = output_dir / "examples"
    if args.save_examples:
        examples_dir.mkdir(parents=True, exist_ok=True)

    converter = aw.SpectrogramConverter()
    expected_shape = (converter.n_mels, converter.image_width)

    metadata = load_metadata(spectrograms_dir)

    source_rows = metadata[
        (metadata["subset"] == args.subset)
        & (metadata["timbres"] == args.source_timbre)
    ].reset_index(drop=True)

    if len(source_rows) == 0:
        raise ValueError(
            f"No source samples found for subset={args.subset!r}, "
            f"source_timbre={args.source_timbre!r}"
        )

    if args.start_index < 0 or args.start_index >= len(source_rows):
        raise IndexError(
            f"start_index={args.start_index} out of range. "
            f"Available range: 0 to {len(source_rows) - 1}"
        )

    source_rows = source_rows.iloc[args.start_index:].reset_index(drop=True)

    if args.max_samples is not None:
        source_rows = source_rows.iloc[:args.max_samples].reset_index(drop=True)

    if args.prompt is None:
        prompt = f"a mel spectrogram of solo {args.target_timbre} music, same melody and rhythm"
    else:
        prompt = args.prompt

    device = "cuda" if torch.cuda.is_available() else "cpu"
    dtype = torch.float16 if device == "cuda" else torch.float32

    pipe = load_pipeline(args=args, device=device, dtype=dtype)

    if args.mode == "external":
        args.external_target_wav_path = prepare_external_target_audio(
            metadata=metadata,
            spectrograms_dir=spectrograms_dir,
            output_dir=output_dir,
            args=args,
            converter=converter,
            device=device,
        )

    rows = []
    skipped = 0

    print("\nEvaluation config:")
    print(f"mode:                {args.mode}")
    print(f"source_timbre:       {args.source_timbre}")
    print(f"target_timbre:       {args.target_timbre}")
    print(f"subset:              {args.subset}")
    print(f"samples:             {len(source_rows)}")
    print(f"lora_path:           {args.lora_path}")
    print(f"strength:            {args.strength}")
    print(f"guidance_scale:      {args.guidance_scale}")
    print(f"num_inference_steps: {args.num_inference_steps}")
    print(f"prompt:              {prompt}")

    if args.mode == "external":
        print(f"external_target_subset: {args.external_target_subset}")
        print(f"external_target_index:  {args.external_target_index}")
        print(f"external_nb_steps:      {args.external_nb_steps}")
        print(f"external_guidance:      {args.external_guidance}")
        print(f"external_target_wav:    {args.external_target_wav_path}")

    print()

    for i, source_row in source_rows.iterrows():
        maestro_id = source_row["maestro_id"]
        slice_id = source_row["slice"]
        source_file = source_row["file_name"]

        print(f"[{i + 1}/{len(source_rows)}] maestro_id={maestro_id}, slice={slice_id}")

        source_spectrogram = load_spectrogram(spectrograms_dir, source_file)

        if source_spectrogram.shape != expected_shape:
            print(f"  Skip: source shape {source_spectrogram.shape}, expected {expected_shape}")
            skipped += 1
            continue

        reference_row = None

        if args.mode == "paired":
            reference_row = find_paired_reference_row(
                metadata=metadata,
                source_row=source_row,
                target_timbre=args.target_timbre,
                subset=args.subset,
            )

            if reference_row is None:
                print("  Skip: no paired reference found")
                skipped += 1
                continue

            reference_spectrogram = load_spectrogram(
                spectrograms_dir,
                reference_row["file_name"],
            )

            reference_file = reference_row["file_name"]

        else:
            reference_spectrogram = make_reference_from_external_waveform(
                row=source_row,
                args=args,
                converter=converter,
            )

            reference_file = "external_model_reference"

        if reference_spectrogram.shape != expected_shape:
            print(f"  Skip: reference shape {reference_spectrogram.shape}, expected {expected_shape}")
            skipped += 1
            continue

        init_image = converter.image_from_spectrogram(source_spectrogram)
        reference_image = converter.image_from_spectrogram(reference_spectrogram)

        generated_image = run_img2img(
            pipe=pipe,
            init_image=init_image,
            prompt=prompt,
            args=args,
            device=device,
            sample_index=i,
        )

        generated_spectrogram = converter.spectrogram_from_image(generated_image)

        metrics = compute_metrics(
            generated_image=generated_image,
            reference_image=reference_image,
            generated_spectrogram=generated_spectrogram,
            reference_spectrogram=reference_spectrogram,
        )

        result_row = {
            "index": i,
            "maestro_id": maestro_id,
            "slice": slice_id,
            "source_timbre": args.source_timbre,
            "target_timbre": args.target_timbre,
            "source_file": source_file,
            "reference_file": reference_file,
            **metrics,
        }

        rows.append(result_row)

        print(
            f"  image_mse={metrics['image_mse']:.6f} | "
            f"image_mae={metrics['image_mae']:.6f} | "
            f"psnr={metrics['image_psnr']:.2f} | "
            f"logspec_mse={metrics['logspec_mse']:.6f}"
        )

        if args.save_examples and len(rows) <= args.num_examples_to_save:
            sample_dir = examples_dir / f"sample_{len(rows):03d}_id_{maestro_id}_slice_{slice_id}"
            sample_dir.mkdir(parents=True, exist_ok=True)

            init_image.save(sample_dir / "source_input.png")
            generated_image.save(sample_dir / "generated_output.png")
            reference_image.save(sample_dir / "reference_target.png")

            np.save(sample_dir / "source_input.npy", source_spectrogram)
            np.save(sample_dir / "generated_output.npy", generated_spectrogram)
            np.save(sample_dir / "reference_target.npy", reference_spectrogram)

            if args.save_audio_examples:
                source_waveform = converter.waveform_from_spectrogram(
                    source_spectrogram,
                    num_griffin_lim_iters=args.num_griffin_lim_iters,
                    device=device,
                )
                generated_waveform = converter.waveform_from_spectrogram(
                    generated_spectrogram,
                    num_griffin_lim_iters=args.num_griffin_lim_iters,
                    device=device,
                )
                reference_waveform = converter.waveform_from_spectrogram(
                    reference_spectrogram,
                    num_griffin_lim_iters=args.num_griffin_lim_iters,
                    device=device,
                )

                save_audio(source_waveform, converter.sample_rate, sample_dir / "source_input.wav")
                save_audio(generated_waveform, converter.sample_rate, sample_dir / "generated_output.wav")
                save_audio(reference_waveform, converter.sample_rate, sample_dir / "reference_target.wav")

    results_df = pd.DataFrame(rows)
    results_csv = output_dir / "evaluation_results.csv"
    results_df.to_csv(results_csv, index=False)

    metric_cols = [
        "image_mse",
        "image_mae",
        "image_psnr",
        "logspec_mse",
        "logspec_mae",
    ]

    if len(results_df) > 0:
        summary = {}

        for col in metric_cols:
            summary[f"{col}_mean"] = float(results_df[col].mean())
            summary[f"{col}_std"] = float(results_df[col].std(ddof=0))
            summary[f"{col}_min"] = float(results_df[col].min())
            summary[f"{col}_max"] = float(results_df[col].max())

        summary["num_evaluated"] = int(len(results_df))
        summary["num_skipped"] = int(skipped)

    else:
        summary = {
            "num_evaluated": 0,
            "num_skipped": int(skipped),
        }

    summary["mode"] = args.mode
    summary["source_timbre"] = args.source_timbre
    summary["target_timbre"] = args.target_timbre
    summary["subset"] = args.subset
    summary["lora_path"] = args.lora_path
    summary["strength"] = args.strength
    summary["guidance_scale"] = args.guidance_scale
    summary["num_inference_steps"] = args.num_inference_steps
    summary["prompt"] = prompt

    if args.mode == "external":
        summary["external_target_subset"] = args.external_target_subset
        summary["external_target_index"] = args.external_target_index
        summary["external_nb_steps"] = args.external_nb_steps
        summary["external_guidance"] = args.external_guidance
        summary["external_target_wav_path"] = str(args.external_target_wav_path)

    with open(output_dir / "evaluation_summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    pd.DataFrame([summary]).to_csv(
        output_dir / "evaluation_summary.csv",
        index=False,
    )

    print("\nDone.")
    print(f"Saved results table: {results_csv}")
    print(f"Saved summary json:  {output_dir / 'evaluation_summary.json'}")
    print(f"Saved summary csv:   {output_dir / 'evaluation_summary.csv'}")

    if len(results_df) > 0:
        print("\nPer-sample results preview:")
        preview_cols = [
            "index",
            "maestro_id",
            "slice",
            "image_mse",
            "image_mae",
            "image_psnr",
            "logspec_mse",
            "logspec_mae",
        ]
        print(results_df[preview_cols].to_string(index=False))

        print("\nSummary:")
        for key, value in summary.items():
            if isinstance(value, float):
                print(f"{key}: {value:.6f}")
            else:
                print(f"{key}: {value}")


if __name__ == "__main__":
    main()