def prepare_external_target_audio(
    metadata: pd.DataFrame,
    spectrograms_dir: Path,
    output_dir: Path,
    args,
    converter: aw.SpectrogramConverter,
    device: str,
) -> Path:
    """
    Pick one target-timbre spectrogram slice from the dataset,
    reconstruct it to wav, and use it as path2 for external model.
    """

    target_rows = metadata[
        (metadata["subset"] == args.external_target_subset)
        & (metadata["timbres"] == args.target_timbre)
    ].reset_index(drop=True)

    if len(target_rows) == 0:
        raise ValueError(
            f"No target style samples found for "
            f"subset={args.external_target_subset!r}, "
            f"target_timbre={args.target_timbre!r}"
        )

    if args.external_target_index < 0 or args.external_target_index >= len(target_rows):
        raise IndexError(
            f"external_target_index={args.external_target_index} out of range. "
            f"Available range: 0 to {len(target_rows) - 1}"
        )

    target_row = target_rows.iloc[args.external_target_index]
    target_file = target_row["file_name"]

    target_spectrogram = load_spectrogram(spectrograms_dir, target_file)

    expected_shape = (converter.n_mels, converter.image_width)
    if target_spectrogram.shape != expected_shape:
        raise ValueError(
            f"Target style spectrogram shape is {target_spectrogram.shape}, "
            f"expected {expected_shape}"
        )

    tmp_dir = output_dir / "external_tmp"
    tmp_dir.mkdir(parents=True, exist_ok=True)

    target_waveform = converter.waveform_from_spectrogram(
        target_spectrogram,
        num_griffin_lim_iters=args.num_griffin_lim_iters,
        device=device,
    )

    target_waveform = np.asarray(target_waveform, dtype=np.float32).squeeze()
    target_waveform = np.nan_to_num(target_waveform)

    peak = np.max(np.abs(target_waveform))
    if peak > 1.0:
        target_waveform = target_waveform / peak

    target_wav_path = (
        tmp_dir
        / f"target_style_{args.target_timbre}_"
          f"index_{args.external_target_index}_"
          f"{Path(target_file).stem}.wav"
    )

    save_audio(
        waveform=target_waveform,
        sample_rate=converter.sample_rate,
        path=target_wav_path,
    )

    print("\nExternal target/style audio:")
    print(f"  target_timbre: {args.target_timbre}")
    print(f"  target_subset: {args.external_target_subset}")
    print(f"  target_index:  {args.external_target_index}")
    print(f"  target_file:   {target_file}")
    print(f"  saved wav:     {target_wav_path}")

    return target_wav_path


def get_external_reference_waveform(
    row: pd.Series,
    args,
    converter: aw.SpectrogramConverter,
) -> np.ndarray:
    """
    Create external reference audio using src.ismir2024.genrerate.

    path1 = reconstructed source test-set wav
    path2 = reconstructed target-timbre style wav chosen from dataset
    """

    from src.ismir2024 import genrerate

    if not hasattr(args, "external_target_wav_path"):
        raise ValueError(
            "args.external_target_wav_path was not prepared. "
            "Call prepare_external_target_audio(...) before the evaluation loop."
        )

    spectrograms_dir = Path(args.spectrograms_dir)
    output_dir = Path(args.output_dir)

    tmp_dir = output_dir / "external_tmp"
    tmp_dir.mkdir(parents=True, exist_ok=True)

    maestro_id = row["maestro_id"]
    slice_id = row["slice"]
    file_name = row["file_name"]

    source_spectrogram_path = spectrograms_dir / file_name
    if not source_spectrogram_path.exists():
        raise FileNotFoundError(f"Source spectrogram not found: {source_spectrogram_path}")

    source_spectrogram = np.load(source_spectrogram_path)

    expected_shape = (converter.n_mels, converter.image_width)
    if source_spectrogram.shape != expected_shape:
        raise ValueError(
            f"Source spectrogram shape is {source_spectrogram.shape}, "
            f"expected {expected_shape}"
        )

    device = "cuda" if torch.cuda.is_available() else "cpu"

    source_waveform = converter.waveform_from_spectrogram(
        source_spectrogram,
        num_griffin_lim_iters=args.num_griffin_lim_iters,
        device=device,
    )

    source_waveform = np.asarray(source_waveform, dtype=np.float32).squeeze()
    source_waveform = np.nan_to_num(source_waveform)

    peak = np.max(np.abs(source_waveform))
    if peak > 1.0:
        source_waveform = source_waveform / peak

    source_wav_path = tmp_dir / f"source_maestro_{maestro_id}_slice_{slice_id}.wav"

    save_audio(
        waveform=source_waveform,
        sample_rate=converter.sample_rate,
        path=source_wav_path,
    )

    audio_out, sr = genrerate(
        path1=str(source_wav_path),
        path2=str(args.external_target_wav_path),
        nb_steps=args.external_nb_steps,
        guidance=args.external_guidance,
    )

    audio_out = np.asarray(audio_out, dtype=np.float32).squeeze()
    audio_out = np.nan_to_num(audio_out)

    if sr != converter.sample_rate:
        audio_out = librosa.resample(
            audio_out,
            orig_sr=sr,
            target_sr=converter.sample_rate,
        )

    # Match your 512x512 spectrogram length convention.
    target_len = converter.image_width * converter.hop_length - 1

    if len(audio_out) > target_len:
        audio_out = audio_out[:target_len]
    elif len(audio_out) < target_len:
        audio_out = np.pad(audio_out, (0, target_len - len(audio_out)), mode="constant")

    peak = np.max(np.abs(audio_out))
    if peak > 1.0:
        audio_out = audio_out / peak

    return audio_out.astype(np.float32)


def make_reference_from_external_waveform(
    row: pd.Series,
    args,
    converter: aw.SpectrogramConverter,
) -> np.ndarray:
    waveform = get_external_reference_waveform(
        row=row,
        args=args,
        converter=converter,
    )

    reference_spectrogram = converter.spectrogram_from_waveform(waveform)

    expected_shape = (converter.n_mels, converter.image_width)
    if reference_spectrogram.shape != expected_shape:
        raise ValueError(
            f"External reference waveform produced spectrogram shape "
            f"{reference_spectrogram.shape}, expected {expected_shape}."
        )

    return reference_spectrogram


