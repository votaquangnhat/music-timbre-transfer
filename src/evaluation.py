def load_metadata(spectrograms_dir: Path) -> pd.DataFrame:
    metadata_path = spectrograms_dir / "spectrogram_metadata.csv"

    if not metadata_path.exists():
        raise FileNotFoundError(f"Metadata file not found: {metadata_path}")

    metadata = pd.read_csv(metadata_path)

    required_columns = {"maestro_id", "slice", "timbres", "file_name", "subset"}
    missing = required_columns - set(metadata.columns)

    if missing:
        raise ValueError(
            f"Metadata missing columns: {sorted(missing)}. "
            f"Found columns: {list(metadata.columns)}"
        )

    return metadata


def load_spectrogram(spectrograms_dir: Path, file_name: str) -> np.ndarray:
    path = spectrograms_dir / file_name

    if not path.exists():
        raise FileNotFoundError(f"Spectrogram file not found: {path}")

    return np.load(path)


def save_audio(waveform: np.ndarray, sample_rate: int, path: Path):
    waveform = np.asarray(waveform, dtype=np.float32).squeeze()
    waveform = np.nan_to_num(waveform)

    peak = np.max(np.abs(waveform))
    if peak > 1.0:
        waveform = waveform / peak

    sf.write(
        file=str(path),
        data=waveform,
        samplerate=sample_rate,
        subtype="PCM_16",
    )


def image_to_float_array(image: Image.Image) -> np.ndarray:
    return np.asarray(image.convert("RGB"), dtype=np.float32) / 255.0


def spectrogram_to_log_array(spectrogram: np.ndarray) -> np.ndarray:
    spectrogram = np.asarray(spectrogram, dtype=np.float32)
    spectrogram = np.maximum(spectrogram, 0.0)
    return np.log1p(spectrogram)


def compute_metrics(
    generated_image: Image.Image,
    reference_image: Image.Image,
    generated_spectrogram: np.ndarray,
    reference_spectrogram: np.ndarray,
) -> dict:
    """
    Lower is better:
        image_mse
        image_mae
        logspec_mse
        logspec_mae

    Higher is better:
        image_psnr
    """

    gen_img = image_to_float_array(generated_image)
    ref_img = image_to_float_array(reference_image)

    image_mse = float(np.mean((gen_img - ref_img) ** 2))
    image_mae = float(np.mean(np.abs(gen_img - ref_img)))

    if image_mse <= 1e-12:
        image_psnr = float("inf")
    else:
        image_psnr = float(10.0 * np.log10(1.0 / image_mse))

    gen_log = spectrogram_to_log_array(generated_spectrogram)
    ref_log = spectrogram_to_log_array(reference_spectrogram)

    logspec_mse = float(np.mean((gen_log - ref_log) ** 2))
    logspec_mae = float(np.mean(np.abs(gen_log - ref_log)))

    return {
        "image_mse": image_mse,
        "image_mae": image_mae,
        "image_psnr": image_psnr,
        "logspec_mse": logspec_mse,
        "logspec_mae": logspec_mae,
    }


def find_paired_reference_row(
    metadata: pd.DataFrame,
    source_row: pd.Series,
    target_timbre: str,
    subset: str,
) -> Optional[pd.Series]:
    matched = metadata[
        (metadata["subset"] == subset)
        & (metadata["timbres"] == target_timbre)
        & (metadata["maestro_id"] == source_row["maestro_id"])
        & (metadata["slice"] == source_row["slice"])
    ]

    if len(matched) == 0:
        return None

    return matched.iloc[0]


